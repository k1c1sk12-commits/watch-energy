// Scans /public/watches for <id>.png files and writes src/lib/imageManifest.ts.
// Runs automatically before `npm run dev` and `npm run build` (predev/prebuild),
// so dropping a PNG into public/watches/ is all that's needed to use it — no
// code edits. Watches without a PNG fall back to the generated SVG illustration.
import { readdirSync, writeFileSync, existsSync, mkdirSync } from "node:fs";

const DIR = "public/watches";
if (!existsSync(DIR)) mkdirSync(DIR, { recursive: true });

const ids = readdirSync(DIR)
  .filter((f) => f.toLowerCase().endsWith(".png"))
  .map((f) => f.replace(/\.png$/i, ""))
  .sort();

const out =
  `// AUTO-GENERATED by scripts/gen-image-manifest.mjs — do not edit by hand.\n` +
  `// Watch ids that have a /public/watches/<id>.png illustration.\n` +
  `export const IMAGE_READY = new Set<string>(${JSON.stringify(ids)});\n`;

writeFileSync("src/lib/imageManifest.ts", out);
console.log(`[image-manifest] ${ids.length} image(s): ${ids.join(", ") || "(none yet)"}`);
